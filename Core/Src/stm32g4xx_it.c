/**
  * @brief This function handles TIM1 trigger and commutation interrupts and TIM17 global interrupt.
  */
void TIM1_TRG_COM_TIM17_IRQHandler(void)
{
  /* USER CODE BEGIN TIM1_TRG_COM_TIM17_IRQn 0 */

  /* USER CODE END TIM1_TRG_COM_TIM17_IRQn 0 */
  HAL_TIM_IRQHandler(&htim17);
  /* USER CODE BEGIN TIM1_TRG_COM_TIM17_IRQn 1 */
	//key_scan(&key1);
	//key_scan2(&key1);
	static uint8_t time100ms;
	key_scan5(&key1);
	if(keep_time_flag)
	{
		keep_time++;
	}
	
	if(time5s_lock_flag!=0)
	{
		time100ms++;
		if(time100ms>10)
		{
				if(M=='L')
				{
					TIM2->ARR =1250+25*(50-(time5s_lock_flag/10));
					TIM2->CCR2 = (TIM2->ARR)*(P/100.f);
				}
				if(M=='H')
				{
					TIM2->ARR =2500-25*(50-(time5s_lock_flag/10));
					TIM2->CCR2 = (TIM2->ARR)*(P/100.f);
				}
			time100ms =0;
			if(((led_show>>1)&0x01)==0x01)
			LED_display(led_show=(led_show&(~(0x02))),0);
			else if(((led_show>>1)&0x01)==0x00)
			LED_display(led_show=(led_show|(0x02)),1);
		}
		time5s_lock_flag--;
	}
	
  /* USER CODE END TIM1_TRG_COM_TIM17_IRQn 1 */
}

